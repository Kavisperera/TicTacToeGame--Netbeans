    private void checkForWinOrDraw() {
        String[][] board = {
            {btn1.getText(), btn2.getText(), btn3.getText()},
            {btn4.getText(), btn5.getText(), btn6.getText()},
            {btn7.getText(), btn8.getText(), btn9.getText()}
        };

        // Check rows, columns, and diagonals
        for (int i = 0; i < 3; i++) {
            if (checkLine(board[i][0], board[i][1], board[i][2])
                    || checkLine(board[0][i], board[1][i], board[2][i])) {
                endGame(board[i][i] + " wins!");
                return;
            }
        }
        if (checkLine(board[0][0], board[1][1], board[2][2])
                || checkLine(board[0][2], board[1][1], board[2][0])) {
            endGame(board[1][1] + " wins!");
            return;
        }

        // Check for draw
        boolean isDraw = true;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (board[i][j].isEmpty()) {
                    isDraw = false;
                    break;
                }
            }
        }
        if (isDraw) {
            endGame("It's a draw!");
        }
    }

    private boolean checkLine(String a, String b, String c) {
        return !a.isEmpty() && a.equals(b) && b.equals(c);
    }

