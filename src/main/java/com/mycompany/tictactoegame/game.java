    private void resetGame() {
        JButton[] buttons = {btn1, btn2, btn3, btn4, btn5, btn6, btn7, btn8, btn9};
        for (JButton button : buttons) {
            button.setText("");
            button.setEnabled(true);
        }
        isXTurn = true;
        lblTurn.setText("X TURN");
    }

